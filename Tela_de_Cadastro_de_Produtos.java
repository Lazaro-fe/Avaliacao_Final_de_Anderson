    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int linhaSelecionada = tabelaProdutos.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um produto na tabela para atualizar!");
            return;
        }

        try {
            // Pega o ID da linha selecionada na tabela (Coluna index 1 é o ID)
            int id = (int) tabelaProdutos.getValueAt(linhaSelecionada, 1); 
            String nome = txtNomeProduto.getText();
            int qtd = Integer.parseInt(txtQuantidade.getText());
            double preco = Double.parseDouble(txtPreco.getText());
            
            Produto_controle controle = new Produto_controle();
            controle.atualizar_produto(id, nome, qtd, preco, 1); // 1 = Categoria padrão
            
            JOptionPane.showMessageDialog(this, "Produto atualizado com sucesso!");
            preencherTabela();
            
            txtNomeProduto.setText("");
            txtQuantidade.setText("");
            txtPreco.setText("");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Quantidade e Preço devem ser numéricos!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar dados: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linhaSelecionada = tabelaProdutos.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um produto na tabela para remover!");
            return;
        }

        try {
            // Pega apenas o ID (coluna 1)
            int id = (int) tabelaProdutos.getValueAt(linhaSelecionada, 1);
            
            Produto_controle controle = new Produto_controle();
            controle.remover_produto(id); // Agora passa só o ID para o controle
            
            JOptionPane.showMessageDialog(this, "Produto removido com sucesso!");
            preencherTabela();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao remover produto: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

