    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       try {
        // Captura o termo digitado no campo de texto de pesquisa e remove espaços extras.
        String termoPesquisa = txtBusca.getText().trim();

        // Instancia a classe de controle para realizar a filtragem dos dados
        Produto_controle controle = new Produto_controle();
        
        // O método 'buscar_produtos_por_nome' já adiciona os caracteres curingas '%' no banco de dados
        // Se o campo estiver vazio, ele trará automaticamente todos os produtos .
        java.util.List<Produto> listaFiltrada = controle.buscar_produtos_por_nome(termoPesquisa);

        // Obtém o modelo da tabela existente na sua interface gráfica 
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tabelaProdutos.getModel();
        
        // Limpa todas as linhas atuais da tabela para evitar duplicações na exibição 
        modelo.setNumRows(0);

        // Percorre a lista filtrada retornada pelo banco e adiciona as novas linhas 
        for (Produto p : listaFiltrada) {
            modelo.addRow(new Object[]{
                p.getNome_usuario(), // Login do usuário associado 
                p.getId(),            // ID do produto 
                p.getNome_do_produto(), // Nome do produto 
                p.getQuantidade(),    // Quantidade em estoque 
                p.getPreco()          // Preço unitário 
            });
        }

        // Opcional: Dá um feedback visual se nenhum registro correspondente for encontrado
        if (listaFiltrada.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nenhum produto encontrado com o termo digitado.");
        }

    } catch (Exception e) {
        // Captura e exibe qualquer falha na comunicação ou no mapeamento SQL
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao realizar a busca de produtos: " + e.getMessage());
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

