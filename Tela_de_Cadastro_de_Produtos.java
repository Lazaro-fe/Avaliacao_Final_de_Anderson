private void preencherTabela() {
    try {
            // Instanciando o Controle em vez do DAO
            Connection conexao_de_banco = new conexao_de_banco().getConnection();
            Produto_controle controle = new Produto_controle();
            java.util.List<Produto> lista = controle.listar_de_Produtos();
            
            // Pega o modelo de colunas da sua tabelaProdutos
            DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tabelaProdutos.getModel();
            modelo.setNumRows(0); // Limpa a tabela para não duplicar os dados na tela
            
            // Coloca os produtos do banco dentro da tabela da tela
            for (Produto p : lista) {
                modelo.addRow(new Object[]{
                    p.getNome_usuario(), // Agora mostra o Login do usuário que cadastrou graças ao JOIN no DAO
                    p.getId(),            
                    p.getNome_do_produto(),
                    p.getQuantidade(),    
                    p.getPreco()          
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados da tabela: " + e.getMessage());
        }
}

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            String nome = txtNomeProduto.getText();
            int qtd = Integer.parseInt(txtQuantidade.getText());
            double preco = Double.parseDouble(txtPreco.getText());
            
            // Instanciando o Controle
            Produto_controle controle = new Produto_controle();
            
            // Passaremos 1 como ID de usuário e categoria como teste/padrão
            controle.cadastrar_produto(nome, qtd, preco, 1, 1);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Produto salvo com sucesso!");
            preencherTabela(); 
            
            // Limpa os campos após salvar
            txtNomeProduto.setText("");
            txtQuantidade.setText("");
            txtPreco.setText("");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Quantidade e Preço devem ser numéricos!");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro inesperado ao cadastrar.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

